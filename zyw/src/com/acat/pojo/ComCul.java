package com.acat.pojo;

public class ComCul {
	private int comid;
	private String introduction;
	private String pricutre;
	private String time;
	public int getComid() {
		return comid;
	}
	public void setComid(int comid) {
		this.comid = comid;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getPricutre() {
		return pricutre;
	}
	public void setPricutre(String pricutre) {
		this.pricutre = pricutre;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
}
